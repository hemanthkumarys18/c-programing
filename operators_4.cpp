#include<iostream>
using namespace std;

/*bit wise operator*/

int main()
{
    int x=18,y=8;
    cout<<"x&y:"<<(x&y)<<endl;
    cout<<"x|y:"<<(x|y)<<endl;
    cout<<"x^y:"<<(x^y)<<endl;
    cout<<"~y:"<<(~y)<<endl;
    cout<<"x>>2:"<<(x>>y)<<endl;
    cout<<"x<<3:"<<(x<<y)<<endl;
    return 0;
}