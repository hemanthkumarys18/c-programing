#include<iostream>
using namespace std;

int main()
{
    int x=3,y=7;
    cout<<"x<Y:"<<(x<y)<<endl;
    cout<<"x>Y:"<<(x>y)<<endl;
    cout<<"x<=Y:"<<(x<=y)<<endl;
    cout<<"x>=Y:"<<(x>=y)<<endl;
    cout<<"x==Y:"<<(x==y)<<endl;
    cout<<"x!=Y:"<<(x!=y)<<endl;
    return 0;
    

}